		<h1>Biographie</h1>
		<div class="bioWrapper">
			<p>C'est à BDBulle, la librairie de la femme de Sokal que Jean David Morvan découvre toute la BD dite adulte en cherchant la suite de Métro Cassiopée direction Châtelet dans la série Valérian. Il a 11 ans et repart avec La terre de la bombe, L'Indien Français, Les Innommables, Bastos et Zakousky, Comanche, L'Incal, Thorgal et surtout le magazine Gomme. Il redouble sa quatrième, il rencontre alors Christian Lerolle, un des deux Color Twins, et lors de sa seconde terminale Frank Guréghian, l'autre moitié du duo. Il commence à collaborer à un fanzine, Hors Gabarit, avec un copain croisé en classe de seconde, Sylvain Savoia. En 1989, Jean David Morvan intègre l'école Saint-Luc de Bruxelles pour apprendre le métier de dessinateur. Dès lors, il arrête de dessiner, et fréquente la librairie Forbidden Zone où il croise de nombreux professionnels de la BD et découvre comics et mangas. Au festival de Villeneuve d'Asc, il s'improvise scénariste pour ses copains, et, à sa grande surprise Reflets Perdus présenté avec Savoia est accepté par les éditions Zenda. L'année suivante, il passe le concours des Beaux-Arts d'où il est "viré pour non-assistance aux cours". Premier album et service militaire en poche, le chemin reste semé d'embûches... Alors que Zenda est racheté par Glénat, Akira arrive en France avec fracas et succès. Morvan propose alors Nomad avec Savoia. Le dessinateur lui présente Buchet qui s'attèle au design. Dans la foulée, son éditeur lui présente Trantkat et HK voit le jour. Pendant un an, la farandole des projets refusés continue jusqu'au jour où Guy Delcourt accepte Sillage. Les anciens projets reprennent vie : 7 secondes avec Parel, Zorn et Dirna en compagnie de Bessadi et Trannoy (Soleil)… Dans le même temps, il réalise avec Munuera des histoires courtes du très hautain Sir Pyle. Suivent des projets plus récents, tels que Le cycle de Tschaï avec Li-An, adaptation d'une série de romans de Jack Vance, La Mandiguerre avec Tamiazzo, Continuum avec Ponce, ou encore Jolin la teigne en compagnie de Ruben. Scénariste boulimique, Morvan n'arrête pas d'imaginer… À venir : Le Bonheur avec Lejeune ou Trois, et l'ange sous le dessin de Colombo, sans oublier un péplum grec mis en scène par Munuera. Viennent de sortir le sixième tome de Sillage, série où chaque album narre une histoire autonome; le cinquième tome du Cycle de Tschaï, ainsi que Lord Clancharlie ( en collaboration avec Delestret), adaptation personelle du roman de Victor Hugo, L'Homme qui Rit. </p>
