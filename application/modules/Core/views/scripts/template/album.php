			<p class="justify">Après la vie à bord d'un baleinier, il nous montre le pénitencier. Il va s'attacher à nous faire subir les pires conditions comme les prisonniers pour mieux nous donner l'envie de s'évader en compagnie d'Esteban et de ses compagnons. Bonhomme s'applique à présenter des personnages forts et qui ont de la présence. Dans ce tome ci, il met en place une femme, la femme du directeur, qui se prend d'affection pour Esteban mais qui semble aussi avoir un problème. Avec cette histoire qui se place à côté de grands classiques comme le Papillon de Schaffner ou Alcatraz de Don Siegel, Bonhomme prouve que c'est un scénariste sur qui il faut compter dorénavant.</p>
			
			<p class="justify">Tout comme, encore une fois, nous sommes sidérés par son travail au dessin. Les personnages ont de la gueule, les décors font ressortir l'hostilité des lieux et il use d'une sacré mise en scène, très grand spectacle. Il fait ressortir toutes les émotions d'un simple trait. C'est simple mais très efficace.</p>
			
			<p class="justify">Ce tome 4 nous donne l'envie de partager l'aventure au côté d'Esteban. Ce récit est des plus réussis.<br>Pour l'occasion, Dupuis ressort les trois premiers tomes sous une nouvelle présentation qui ravira les collectionneurs.<br>A ne pas rater !</p>
			
