			<p class="justify answer">Matthieu Bonhomme : J’ai toujours été un passionné de la mer. Je suis aussi un peu navigateur à mes heures. À la voile, bien sûr, j’ai des souvenirs merveilleux de croisières avec mes frères ou des amis proches. Ce sont pour moi autant d’aventures vécues. Et puis il y a mes lectures de jeunesse : l’île au trésor, Moby Dick… C’est tout naturellement donc que j’ai été tenté très tôt de poser un récit à bord d’un navire baleinier. J’ai eut cette idée en tête pendant de longues années sans trouver la « porte d’entrée » du projet. J’étais plein d’inibitions.</p>
