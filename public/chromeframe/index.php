		<h1>Qu'est-ce que Google Chrome Frame ?</h1>
		
		<p>Google propose aux utilisateurs d'Internet Explorer, une extension <strong>gratuite</strong> et <strong>rapide à installer</strong>. Celle-ci vous permettra d'améliorer le rendu visuel de vos sites web préférés, <strong>sans changer de navigateur</strong>.</p>
		
		<h1>Pourquoi Google Chrome Frame ?</h1>
		
		<p>Internet Explorer, jusqu'à sa version 9 ne respecte pas un bon nombre de standards qui font le web d'aujourd'hui. Il est ainsi devenu difficile pour les concepteurs web de proposer un site compatible avec les vieilles versions d'Internet Explorer.</p>
		
		<p>Pour remédier à ce problème, Google est intervenu en 2009 en proposant Chrome Frame. Cette extension permet aux utilisateurs d'Internet Explorer de conserver leur navigateur, tout en profitant des dernières avancées technologiques, qui font le web d'aujourd'hui.</p>
		
